const getUrl = (title) => {
  if (!title) return null;
  const prepared = title
    .trim()
    .replace(/[\s,!?#|*&~/\//\\]/g, "-")
    .toLowerCase();
  const result = prepared.split("").map((char) => words[char] || char);
  return result.join("");
};

export default getUrl;
